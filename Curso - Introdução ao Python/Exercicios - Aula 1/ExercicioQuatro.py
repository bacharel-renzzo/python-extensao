# Ex4 - Fazer um programa em que o usuário entra com o ano de seu nascimento e o programa imprime na tela ssua idade.

# Declaração da variável
print("Informe, por gentileza, o ano de seu nascimento")
ano_de_nascimento = int(input())

# Calcular a idade
idade = 2023 - ano_de_nascimento

# Imprimir na tela a idade
print("Você possui:", idade, "anos")
