# Ex1 -  Fazer programa que converte a velocidade de m/s para km/h. Dado: velocidade(m/s) = velocidade(km/h) * 3.6

# Declaração da variável velocidade
print("Informe, por gentileza, a velocidade em m/s que você deseja converter para km/h")
velocidade_m_s = float(input())

# Conversão da velocidade
velocidade_k_h = velocidade_m_s * 3.6

# Imprimir na tela a velocidade convertida
print("A velocidade convertida para km/h é: ", velocidade_k_h )





