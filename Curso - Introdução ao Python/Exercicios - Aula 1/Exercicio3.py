# Ex3 - Fazer um programa em que o usuário entra com um número e o programa imprime na tela seu antecessor e seu sucessor.

# Declaração da variável
print("Informe, por gentileza, um número: ")
numero = float(input())

# Calcular o antecessor
antecessor = numero - 1

# Calcular o sucessor
sucessor = numero + 1 

# Imprimir na tela
print("O sucessor do número escolhido é: ", sucessor)
print("O antecessor do número escolhido é: ", antecessor)

